public class ifelse {
    public static void main(String[] args) {
        int number = 17;

        if (number > 18) {
            System.out.println("Eligible to vote.");
        } else if (number < 18) {
            System.out.println("Not eligible to vote.");
        } else {
            System.out.println("Just eligible to vote.");
        }
    }
}
